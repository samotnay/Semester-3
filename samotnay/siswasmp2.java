        txtNis.setText(nim);

        String nama = tabelSiswa.getValueAt(baris, 2).toString();
        txtNama.setText(nama);

        String kelas = tabelSiswa.getValueAt(baris, 3).toString();
        cbKelas.setSelectedItem(kelas);
        
        String kelamin = tabelSiswa.getValueAt(baris, 4).toString();
        cbKelamin.setSelectedItem(kelamin);

        String alamat = tabelSiswa.getValueAt(baris, 5).toString();
        txtAlamat.setText(alamat);

        String email = tabelSiswa.getValueAt(baris, 6).toString();
        txtEmail.setText(email);

        String telp = tabelSiswa.getValueAt(baris, 7).toString();
        txtTelp.setText(telp);
        
        
        tab1.setSelectedIndex(i); //muleh nk tab 1
